// Control Statement

/*
control Statement keywords
if,else,switch,case,break,continue,default,for,while,do
if example : -
Syntax:-
if(condition)
    True Statement Will Execute
Example:-
    if(10>4)
        console.log("Hello!");

if_else
    Syntax:-
    if(condition)
        true statement will go here...
    else
        false statement will go here...

    Example:-
    Write a program to find greater value between value.
    var a = 10;
    var b = 20;
    if(a>b)
        console.log("A is greater!");
    else    
        console.log("B is Greater!");

    Another Example:-
    You can write more than 1 statement in a curly braces body
            var a = 10;
            var b = 20;
            if(a>b){
                console.log("A is greater!");
                console.log("Hello India")
                }
            else  {
                console.log("B is Greater!");
                console.log("Hello World!");
            }
*/

